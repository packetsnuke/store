    <h2 class="response">How to update bundled plugins</h2>
    <h3>Once you updated the theme, a message should notice that you need to update one of more plugins included with the theme. In order to update the plugins, please read the follow instructions.</h3>

    <p>The plugins package are stored within the following theme folder: <strong><?php echo YIT_THEME_NAME ?>/theme/plugins/</strong>.
        First of all copy the zip file of the plugins you're going to update and paste them in a different place (eg. Desktop folder).
        Now repeat the following instructions for each plugin you need to update.</p>
    <ol>
        <li>Extract the folder contained within the package.
            <p></p><div style="width: 310px" class="wp-caption aligncenter"><a href="http://cdn.yithemes.com/images/plugins/extract_plugin.jpg"><img alt="extract_plugin" src="http://cdn.yithemes.com/images/plugins/extract_plugin.jpg" class="size-medium"></a></div>
        </li>
        <li>Connect with <em>Filezilla</em> to your Wordpress installation and direct to the folder <strong>wp-content/plugins/</strong>.
            <p></p><div style="width: 310px" class="wp-caption aligncenter"><a href="http://cdn.yithemes.com/images/plugins/filezilla_plugins.jpg"><img alt="filezilla_plugins" src="http://cdn.yithemes.com/images/plugins/filezilla_plugins.jpg" class="size-medium"></a></div>
        </li>
        <li>Click twice on your plugin local folder.
            <p></p><div style="width: 310px" class="wp-caption aligncenter"><a href="http://cdn.yithemes.com/images/plugins/filezilla_local.jpg"><img alt="filezilla_local" src="http://cdn.yithemes.com/images/plugins/filezilla_local.jpg" class="size-medium"></a></div>
        </li>
        <li>Drop the local folder within the plugins folder of your server.
            <p></p><div style="width: 310px" class="wp-caption aligncenter"><a href="http://cdn.yithemes.com/images/plugins/filezilla_drop.jpg"><img alt="filezilla_drop" src="http://cdn.yithemes.com/images/plugins/filezilla_drop.jpg" class="size-medium"></a></div>
        </li>
        <li>Overwrite all files.
            <p></p><div style="width: 310px" class="wp-caption aligncenter"><a href="http://cdn.yithemes.com/images/plugins/filezilla_overwrite.jpg"><img alt="filezilla_overwrite" src="http://cdn.yithemes.com/images/plugins/filezilla_overwrite.jpg" class="size-medium"></a></div>
        </li>
        <li>That's all! Now your plugin is updated. Repeat the steps for each plugin you need to update.</li>
    </ol>


